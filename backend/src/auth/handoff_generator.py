_MERMAID_FLOW = """\
sequenceDiagram
    participant U as User
    participant R as RoboScope
    participant I as Identity Provider (IdP)

    U->>R: Click "Sign in with <Provider>"
    R->>I: Authorization Request (client_id, redirect_uri, scopes)
    I-->>U: Login & Consent page
    U->>I: Credentials + Consent
    I-->>R: Authorization Code (callback URL)
    R->>I: Token Exchange (code + client_secret)
    I-->>R: id_token + access_token
    R->>R: Validate id_token, extract groups
    R-->>U: Session cookie + redirect to app
"""


def _get_locale(lang: str) -> HandoffLocale:
    return _LOCALES.get(lang, _LOCALES["en"])


def _callback_url(base_url: str) -> str:
    """Build callback URL from the request base_url (includes trailing slash)."""
    return f"{base_url.rstrip('/')}/auth/sso/callback"


# ---------------------------------------------------------------------------
# Markdown generator
# ---------------------------------------------------------------------------


def generate_markdown(idp: IdentityProvider, base_url: str, lang: str) -> str:
    loc = _get_locale(lang)
    provider_desc = loc["provider_types"].get(idp.provider_type, idp.provider_type)
    callback = _callback_url(base_url)

    lines: list[str] = [
        f"# {loc['title']}",
        "",
        f"**{provider_desc}**",
        "",
        loc["intro"],
        "",
        "---",
        "",
        f"## {loc['callback_url_label']}",
        "",
        f"```\n{callback}\n```",
        "",
        f"_{loc['callback_url_note']}_",
        "",
        f"## {loc['scopes_label']}",
        "",
        f"```\n{idp.scopes}\n```",
        "",
        f"## {loc['group_claim_label']}",
        "",
        f"```\n{idp.group_claim_name}\n```",
        "",
        f"## {loc['group_naming_label']}",
        "",
        loc["group_naming_text"],
        "",
        f"## {loc['test_login_label']}",
        "",
    ]
    for i, step in enumerate(loc["test_login_steps"], 1):
        lines.append(f"{i}. {step}")

    lines += [
        "",
        "---",
        "",
        f"## {loc['oidc_flow_title']}",
        "",
        "```mermaid",
        _MERMAID_FLOW.strip(),
        "```",
        "",
        "---",
        "",
        f"_{loc['footer']}_",
    ]
    return "\n".join(lines)


