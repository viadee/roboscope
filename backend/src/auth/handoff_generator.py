class _OidcFlowDiagram(Flowable):
    """Minimal OIDC flow diagram drawn with reportlab primitives."""

    WIDTH = 14 * cm
    HEIGHT = 6 * cm
    ACTORS = ["User", "RoboScope", "IdP"]
    STEPS = [
        ("U", "R", "1. Login click"),
        ("R", "I", "2. Auth request"),
        ("I", "U", "3. Login page"),
        ("U", "I", "4. Credentials"),
        ("I", "R", "5. Auth code"),
        ("R", "I", "6. Token exchange"),
        ("I", "R", "7. id_token"),
        ("R", "U", "8. Session"),
    ]

    def wrap(self, availWidth: float, availHeight: float) -> tuple[float, float]:
        return self.WIDTH, self.HEIGHT

    def draw(self) -> None:
        w, h = self.WIDTH, self.HEIGHT
        col_w = w / 3

        # Actor boxes
        for i, actor in enumerate(self.ACTORS):
            x = i * col_w + col_w / 2
            bw, bh = 2.2 * cm, 0.6 * cm
            self.canv.setFillColor(colors.HexColor("#1A2D50"))
            self.canv.rect(x - bw / 2, h - bh, bw, bh, fill=1, stroke=0)
            self.canv.setFillColor(colors.white)
            self.canv.setFont("Helvetica-Bold", 8)
            self.canv.drawCentredString(x, h - bh + 0.15 * cm, actor)

        # Lifelines
        self.canv.setStrokeColor(colors.HexColor("#cccccc"))
        self.canv.setLineWidth(0.5)
        for i in range(3):
            x = i * col_w + col_w / 2
            self.canv.line(x, 0, x, h - 0.6 * cm)

        # Steps
        _col_idx = {"U": 0, "R": 1, "I": 2}
        step_h = (h - 0.6 * cm - 0.2 * cm) / len(self.STEPS)
        self.canv.setFont("Helvetica", 6.5)
        self.canv.setStrokeColor(colors.HexColor("#3B7DD8"))
        self.canv.setFillColor(colors.HexColor("#333333"))
        for idx, (src, dst, label) in enumerate(self.STEPS):
            y = h - 0.6 * cm - 0.2 * cm - idx * step_h - step_h * 0.7
            x1 = _col_idx[src] * col_w + col_w / 2
            x2 = _col_idx[dst] * col_w + col_w / 2
            # Arrow line
            self.canv.setLineWidth(1)
            self.canv.line(x1, y, x2, y)
            # Arrowhead
            direction = 1 if x2 > x1 else -1
            self.canv.setFillColor(colors.HexColor("#3B7DD8"))
            p = self.canv.beginPath()
            p.moveTo(x2, y)
            p.lineTo(x2 - direction * 6, y + 3)
            p.lineTo(x2 - direction * 6, y - 3)
            p.close()
            self.canv.drawPath(p, fill=1, stroke=0)
            # Label
            mid_x = (x1 + x2) / 2
            self.canv.setFillColor(colors.HexColor("#333333"))
            self.canv.drawCentredString(mid_x, y + 2, label)


def generate_pdf(idp: IdentityProvider, base_url: str, lang: str) -> bytes:
    loc = _get_locale(lang)
    provider_desc = loc["provider_types"].get(idp.provider_type, idp.provider_type)
    callback = _callback_url(base_url)

    buf = BytesIO()
    doc = SimpleDocTemplate(
        buf,
        pagesize=A4,
        leftMargin=2 * cm,
        rightMargin=2 * cm,
        topMargin=2 * cm,
        bottomMargin=2 * cm,
    )

    styles = getSampleStyleSheet()
    title_style = ParagraphStyle(
        "HandoffTitle",
        parent=styles["Heading1"],
        fontName="Helvetica-Bold",
        fontSize=16,
        textColor=colors.HexColor("#1A2D50"),
        spaceAfter=6,
    )
    h2_style = ParagraphStyle(
        "HandoffH2",
        parent=styles["Heading2"],
        fontName="Helvetica-Bold",
        fontSize=11,
        textColor=colors.HexColor("#1A2D50"),
        spaceBefore=12,
        spaceAfter=4,
    )
    body_style = ParagraphStyle(
        "HandoffBody",
        parent=styles["Normal"],
        fontName="Helvetica",
        fontSize=9,
        leading=13,
        spaceAfter=4,
    )
    mono_style = ParagraphStyle(
        "HandoffMono",
        parent=styles["Code"],
        fontName="Courier",
        fontSize=9,
        backColor=colors.HexColor("#f4f4f4"),
        borderColor=colors.HexColor("#cccccc"),
        borderWidth=0.5,
        borderPadding=4,
        spaceAfter=4,
    )
    note_style = ParagraphStyle(
        "HandoffNote",
        parent=styles["Normal"],
        fontName="Helvetica-Oblique",
        fontSize=8,
        textColor=colors.HexColor("#666666"),
        spaceAfter=6,
    )
    footer_style = ParagraphStyle(
        "HandoffFooter",
        parent=styles["Normal"],
        fontName="Helvetica-Oblique",
        fontSize=7,
        textColor=colors.HexColor("#999999"),
        spaceBefore=12,
    )

    story: list = [
        Paragraph(loc["title"], title_style),
        Paragraph(f"<b>{provider_desc}</b>", body_style),
        Spacer(1, 0.2 * cm),
        Paragraph(loc["intro"], body_style),
        Spacer(1, 0.3 * cm),
        Paragraph(loc["callback_url_label"], h2_style),
        Paragraph(callback, mono_style),
        Paragraph(loc["callback_url_note"], note_style),
        Paragraph(loc["scopes_label"], h2_style),
        Paragraph(idp.scopes, mono_style),
        Paragraph(loc["group_claim_label"], h2_style),
        Paragraph(idp.group_claim_name, mono_style),
        Paragraph(loc["group_naming_label"], h2_style),
        Paragraph(loc["group_naming_text"], body_style),
        Paragraph(loc["test_login_label"], h2_style),
    ]

    for i, step in enumerate(loc["test_login_steps"], 1):
        story.append(Paragraph(f"{i}.&nbsp;&nbsp;{step}", body_style))

    story += [
        Spacer(1, 0.3 * cm),
        Paragraph(loc["oidc_flow_title"], h2_style),
        Spacer(1, 0.2 * cm),
        _OidcFlowDiagram(),
        Spacer(1, 0.3 * cm),
        Paragraph(loc["footer"], footer_style),
    ]

    doc.build(story)
    return buf.getvalue()
