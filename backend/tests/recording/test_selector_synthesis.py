"""Story S.2 — selector synthesis + quality scoring.

Locks in the AR-7 scoring rubric. These tests are the source of truth
for the editor's selector-picker colour dots. If the rubric changes,
update the doc AND these tests together.
"""

from __future__ import annotations

from src.recording.selector_synthesis import (
    AncestorRef,
    ElementSnapshot,
    synthesise_selectors,
)


def _snap(**kwargs) -> ElementSnapshot:
    kwargs.setdefault("tag", "button")
    kwargs.setdefault("attributes", {})
    return ElementSnapshot(**kwargs)


class TestTestidStrategy:
    def test_stable_testid_wins(self) -> None:
        cands = synthesise_selectors(
            _snap(attributes={"data-testid": "submit-btn"})
        )
        testid = [c for c in cands if c.strategy == "testid"]
        assert len(testid) == 1
        assert testid[0].quality_score == 95
        assert testid[0].value == '[data-testid="submit-btn"]'

    def test_all_configured_attrs_emitted(self) -> None:
        cands = synthesise_selectors(
            _snap(attributes={"data-test-id": "a", "data-qa": "b"})
        )
        emitted = {c.value for c in cands if c.strategy == "testid"}
        assert '[data-test-id="a"]' in emitted
        assert '[data-qa="b"]' in emitted

    def test_autogenerated_hash_testid_is_penalised(self) -> None:
        """20+ chars with mixed case+digits → penalty."""
        cands = synthesise_selectors(
            _snap(attributes={"data-testid": "K8aB9cD1eF2gH3iJ4kL5m"})
        )
        testid = [c for c in cands if c.strategy == "testid"][0]
        assert testid.quality_score == 95 - 25


