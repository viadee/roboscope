/**
 * Chinese (Simplified) locale — 中文（简体）.
 *
 * Full translation: every top-level section of the English base is translated
 * here. Values are still deep-merged OVER the English base so any key added to
 * `en.ts` in the future resolves (falls back to English) until translated —
 * structural completeness is guaranteed (no missing keys, no German leakage),
 * pinned by `ZhLocaleParity.spec.ts`.
 *
 * vue-i18n reserved chars: placeholders like {name} are preserved verbatim,
 * `@` is escaped as `{'@'}`, a literal `{` as `\{`, and `|` separates plural
 * forms — all exactly as in the English source. The production build breaks on
 * incorrect escaping, so keep these identical to en.ts.
 */
import en from './en'

type Dict = { [k: string]: string | Dict }

function deepMerge<T extends Dict>(base: T, over: Dict): T {
  const out: Dict = Array.isArray(base) ? base : { ...base }
  for (const key of Object.keys(over)) {
    const o = over[key]
    const b = (out as Dict)[key]
    if (o && typeof o === 'object' && b && typeof b === 'object') {
      out[key] = deepMerge(b as Dict, o as Dict)
    } else {
      out[key] = o
    }
  }
  return out as T
}

